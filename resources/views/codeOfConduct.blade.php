        <h2 class="h5 mt-4 mb-3">How the Code works</h2>

        <p>The Code sets out qualities and abilities registered migration agents (RMAs) must have to be on our Register. It also sets out requirements that you work with clients in a professional and ethical way.</p>

        <p>The Code guides how you:</p>
        <ul>
          <li>interact with your clients</li>
          <li>communicate your fees and charges</li>
          <li>keep records and handle client monies</li>
          <li>work with other agents</li>
          <li>work with any employees you may have</li>
          <li>respond if a client complains about you.</li>
        </ul>

        <p>If you breach the Code, the OMARA may discipline you. If the OMARA decides disciplinary action is warranted, the OMARA may do one of the following:</p>
        <ul>
          <li>caution you</li>
          <li>suspend your registration</li>
          <li>cancel your registration</li>
          <li>bar you from re-registering for a period of up to 5 years.</li>
        </ul>
